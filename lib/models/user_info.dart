class UserInfo {
  final String name;
  final String email;
  final String phoneNo;
  final String gender;
  final int age;
  final String about;
  final String interest;
  final String address;

  UserInfo({
    required this.name,
    required this.email,
    required this.phoneNo,
    required this.gender,
    required this.age,
    required this.about,
    required this.interest,
    required this.address,
  });
}
